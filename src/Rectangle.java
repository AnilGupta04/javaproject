public class Rectangle{
      public int lenth;
      public int width;
 

      public Rectangle(int lenth ,int width){
           this.lenth=length;
           this.width=width;
      }
  
      public int getArea(){
             return lenth*width;
      }
   
      public int getPerimeter(){
         return 2 *(lenth + width);
      }
   }
